// Helper function to get puja by slug
export function getPujaBySlug(slug) {
  return pujaData.find((puja) => puja.slug === slug);
}

// Helper function to get random pujas excluding a specific slug
export function getRandomPujas(excludeSlug, count = 3) {
  const filtered = pujaData.filter((puja) => puja.slug !== excludeSlug);
  const shuffled = [...filtered].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count);
}

